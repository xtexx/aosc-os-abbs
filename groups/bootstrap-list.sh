I=1
. /etc/autobuild/ab4cfg.sh
if [ "$ABSTAGE2" = "1" ] ; then
	MODIFIER=":+stage2"
fi
for pkg in "${ARR[@]}" ; do
	if [ -n "$FROM" ] && [ "$FROM" != "$pkg" ] ; then
		echo "[+] [$I/${#ARR[@]}] Skipping $pkg ..." >&2
		I=$(( I + 1 ))
		continue
	fi
	FROM=
	echo "[+] [$I/${#ARR[@]}] Building $pkg ..." >&2
	echo -ne "\e]0;[$I/${#ARR[@]}] Building $pkg\007\r" >&2
	acbs-build "$pkg$MODIFIER" || {
		echo "[!] Build failed for $pkg!" >&2
		exit 1
	}
	I=$(( I + 1 ))
done
